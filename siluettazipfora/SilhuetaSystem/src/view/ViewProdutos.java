    private void BbuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BbuscarActionPerformed

        Produtos obj = new Produtos();
        ProdutosDAO dao = new ProdutosDAO();

        obj = dao.consultaPorId(Integer.parseInt(TXTbuscaid.getText()));

        if(obj.getId()>= 1){

            TXTid.setText(String.valueOf(obj.getId()));
            TXTcodigo.setText(obj.getCodigo());
            TXTreferencia.setText(obj.getReferencia());
            TXTmodelo.setText(obj.getModelo());
            TXTcor.setText(obj.getCor());
            TXTfornecedor.setText(obj.getFornecedor());
            TXTtamanho.setText(obj.getTamanho());
            TXTtipo.setText (obj.getTipo());
            TXTquantidade.setText(String.valueOf(obj.getQuantidade()));
            TXTcusto.setText(String.valueOf(obj.getCusto()));

        } else {
            JOptionPane.showMessageDialog(null, "Erro: Cliente NÃ£o Encontrado.");
        }
    }//GEN-LAST:event_BbuscarActionPerformed

