    public void listar(){
        
        ProdutosDAO dao = new ProdutosDAO();
        List<Produtos> lista = dao.listarProdutos();
        DefaultTableModel dados = (DefaultTableModel) TABprodutos.getModel();
        dados.setNumRows(0);
        
        for (Produtos p: lista){
            dados.addRow(new Object[]{
                p.getId(),
                p.getCodigo(),        
                p.getReferencia(),
                p.getCusto(),
                p.getQuantidade(),
                p.getFornecedor(),
                p.getModelo(),
                p.getTipo(),
                p.getTamanho(),
                p.getCor(),
                        
        });
        }
    }
    
