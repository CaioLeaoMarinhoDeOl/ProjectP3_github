    private void BsalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BsalvarActionPerformed
        try {
            Produtos obj = new Produtos();
            char ultimoCaractere = TXTtipo.getText().charAt(TXTtipo.getText().length() - 1);
            String codigo = TXTfornecedor.getText().toUpperCase() + TXTreferencia.getText() + ultimoCaractere + TXTtamanho.getText().toUpperCase() + TXTcor.getText().toUpperCase().substring(0,3);
            
            obj.setCodigo (codigo); 
            obj.setReferencia (TXTreferencia.getText());
            obj.setCusto(Double.parseDouble  (TXTcusto.getText()));
            obj.setQuantidade    (Integer.parseInt    (TXTquantidade.getText()));            
            obj.setFornecedor  (TXTfornecedor.getText());                       
            obj.setModelo(TXTmodelo.getText());
            obj.setTipo(TXTtipo.getText());
            obj.setTamanho(TXTtamanho.getText());
            obj.setCor(TXTcor.getText());
            
            
            ProdutosDAO dao = new ProdutosDAO();
            dao.cadastrarProduto(obj);
            
            new Utilitarios().limpaTela(PAINELcadastrarclientes);
            
        } catch (Exception e) {
        }
    }//GEN-LAST:event_BsalvarActionPerformed

