    private void BfiltrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BfiltrarActionPerformed

        String referencia = "%" + TXTpesquisarref.getText() + "%";
        String modelo = "%" + TXTpesquisarmod.getText() + "%";
        String tamanho = "%" + TXTpesquisartam.getText() + "%";
        String cor = "%" + TXTpesquisarcor.getText() + "%";
        
        ProdutosDAO dao = new ProdutosDAO();
        List<Produtos> lista = dao.buscaProdutosPorFiltro(referencia,modelo,tamanho,cor);
        DefaultTableModel dados = (DefaultTableModel) TABprodutos.getModel();
        dados.setNumRows(0);
        
        for (Produtos p: lista){
            dados.addRow(new Object[]{
                p.getId(),
                p.getCodigo(),
                p.getReferencia(),
                p.getCusto(),
                p.getQuantidade(),
                p.getFornecedor(),
                p.getModelo(),
                p.getTipo(),
                p.getTamanho(),
                p.getCor(), 
                
        });
        }
        
    }//GEN-LAST:event_BfiltrarActionPerformed

